#!/usr/bin/env python3
"""
Module 1-concat

 A type-annotated function concat that takes
 a string str1 and a string str2 as arguments
 and returns a concatenated string
"""


def concat(str1: str, str2: str) -> str:
    """
    Conctenates str1 and str2

    Args:
        str1 (str)
        str2 (str)

    Returns:
        str
    """
    return str1 + str2
