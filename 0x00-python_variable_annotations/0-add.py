#!/usr/bin/env python3
"""
Module 0-add

 a type-annotated function add that takes
 a float a and a float b as arguments
 and returns their sum as a float.
"""


def add(a: float, b: float) -> float:
    """
    Sums the vales of a and b

    Args:
        a (float)
        b (float)

    Returns:
        float
    """
    return a + b
